function add(x,y) {
    return x + y;
}

function subtract(x,y) {
    return x - y;
}

function multiply(x,y) {
    return x * y;
}

function divide(x,y) {
    return x / y;
}

function increment(i) {
    let number = i;
    number++;
    return number;
}

function decrement(i) {
    let number = i;
    number--;
    return number;
}

function makeInt(i) {
    return parseInt(i, 10);
}

function preserveDecimal(i) {
    return parseFloat(i);
}